use core::ffi::c_void;
use crate::resource_bundle_handler::ScaleFactor;
use crate::string::CefString;
use cef_sys::{cef_resource_bundle_t, cef_resource_bundle_get_global};
use std::{slice, ptr};

ref_counted_ptr!{
    /// Structure used for retrieving resources from the resource bundle (*.pak)
    /// files loaded by CEF during startup or via the cef_resource_bundle_tHandler
    /// returned from cef_app_t::GetResourceBundleHandler. See CefSettings for
    /// additional options related to resource bundle loading. The functions of this
    /// structure may be called on any thread unless otherwise indicated.
    pub struct ResourceBundle(*mut cef_resource_bundle_t);
}

impl ResourceBundle {
    /// Returns the global resource bundle instance.
    pub fn global() -> ResourceBundle {
        unsafe {
            ResourceBundle::from_ptr_unchecked(cef_resource_bundle_get_global())
        }
    }

    /// Returns the localized string for the specified `string_id` or an `None`
    /// string if the value is not found. Include cef_pack_strings.h for a listing
    /// of valid string ID values.
    pub fn get_localized_string(&self, string_id: i32) -> Option<String> {
        unsafe {
            CefString::from_userfree(self.0.get_localized_string.unwrap()(self.as_ptr(), string_id as _)).map(String::from)
        }
    }
    /// Retrieves the contents of the specified scale independent `resource_id`. If
    /// the value is found then this function will return `Some(data)`. If the value is not found then this function
    /// will return `None`.
    pub fn get_data_resource(&self, resource_id: i32) -> Option<&'static [u8]> {
        unsafe {
            let mut data_ptr = ptr::null_mut();
            let mut size = 0;
            let exists = self.0.get_data_resource.unwrap()(self.as_ptr(), resource_id, &mut data_ptr, &mut size) != 0;
            if exists {
                Some(slice::from_raw_parts(data_ptr as *const c_void as *const u8, size))
            } else {
                None
            }
        }
    }
    /// Retrieves the contents of the specified `resource_id` nearest the scale
    /// factor `scale_factor`. Use a `scale_factor` value of `None` for
    /// scale independent resources or call `get_data_resource` instead. If the value
    /// is found then this function will return `Some(data)`. If the value is not found then this function will
    /// return `None`.
    pub fn get_data_resource_for_scale(&self, resource_id: i32, scale_factor: Option<ScaleFactor>) -> Option<&'static [u8]> {
        unsafe {
            let mut data_ptr = ptr::null_mut();
            let mut size = 0;
            let exists = self.0.get_data_resource_for_scale.unwrap()(self.as_ptr(), resource_id, scale_factor.map(|s| s as crate::CEnumType).unwrap_or(0), &mut data_ptr, &mut size) != 0;
            if exists {
                Some(slice::from_raw_parts(data_ptr as *const c_void as *const u8, size))
            } else {
                None
            }
        }
    }
}

